#include "ft_printf.h"

void	p_str(char *s, int *count)
{
	int	i;

	i = 0;
	if (!s)
	{
		write(1, "(null)", 6);
		*count += 6;
		return ;
	}
	while (s[i])
	{
		p_char(s[i], count);
		i++;
	}
}
